def area(width, height):
    result = width * height
    return result

result = area(5, 6)
print(result)

result = area(9, 6)
print(result)
result = area(6, 3)
print(result)

def subtract(num1, num2):
    result = num1 - num2
    return result

result = subtract(73, 8)
print(result)
result = subtract(6, 5)
print(result)

def divide(number_one, number_two):
    result = number_one / number_two
    return result

def add(num1, num2):
    result = num1 + num2
    return result

result = add(12, 4)
print(result)
result = add(15, 14)
print(result)